    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model=( DefaultTableModel )ptable.getModel();
        try {
            Class.forName("java.sql.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost/dwdts","root","kvalwar");
            Statement stmt=con.createStatement() ;
            String query="select * from purchse order by sno asc ;";
            ResultSet rs=stmt.executeQuery(query) ;
            while(rs.next()) {
                String seno, dt, armno, name, post, wcode, quant, prce  ;
                seno=rs.getString("sno");
                dt=rs.getString("date");
                armno=rs.getString("Acode");
                name=rs.getString("name");
                post=rs.getString("post");
                wcode=rs.getString("wcode");
                quant=rs.getString("qty");
                prce=rs.getString("price");
                model.addRow(new Object[] {seno, dt, armno, name, post, wcode, quant, prce});
            }
            rs.close();
            stmt.close();
            con.close();
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null,"Error In connectivity");
        }
}//GEN-LAST:event_jButton3ActionPerformed

